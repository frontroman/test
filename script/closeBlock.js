document.addEventListener("DOMContentLoaded", function(){
    document.getElementById("close__btn-about").addEventListener("click", function(){
        let elem = document.getElementById("about");
        let openBtn = document.getElementById("open__btn-about");
        let closeBtn = document.getElementById("close__btn-about");
        closeBtn.style.display = "none";
        openBtn.style.display = "block";
        elem.style.display = "none";
    })
})

